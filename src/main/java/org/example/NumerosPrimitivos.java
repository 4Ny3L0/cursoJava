package org.example;

public class NumerosPrimitivos {
    public static void main(String[] args) {
        byte numeroByte = 127;
        /*Un byte puede almacenar valores desde el -128 hasta 127*/
        System.out.println(numeroByte);
        short numberShort = 12342;
        System.out.println(numberShort);

        int numeroInt = 239234;
        System.out.println(numeroInt);

        long numberLong = 823234234243242L;
        System.out.println(numberLong);
    }
}
